// Вспомогательные функции
export function getGameById(id: string): GameConfig | undefined {
  return GAMES_CONFIG.find(game => game.id === id);
}

export function getGamesByCategory(category: string): GameConfig[] {
  if (category === 'all') return GAMES_CONFIG;
  return GAMES_CONFIG.filter(game => game.category === category);
}

export function getGamesByDifficulty(min: number, max: number): GameConfig[] {
  return GAMES_CONFIG.filter(game => game.difficulty >= min && game.difficulty <= max);
}

export function getRandomGame(): GameConfig {
  return GAMES_CONFIG[Math.floor(Math.random() * GAMES_CONFIG.length)];
}